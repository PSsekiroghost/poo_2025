package aula2_comentarios;

//Este código é de abstração
//Representa um modelo (classe) para o objeto Carro, com seus atributos básicos.

public class Carro {
	
	// Atributos públicos que definem as características de um carro.
	// Esses atributos representam o estado do objeto.
	public String marca;      // Marca do carro (ex: Fiat, Honda)
	public String modelo;     // Modelo do carro (ex: Pulse, Civic)
	public int ano;           // Ano de fabricação
	public float preco;       // Preço do carro
	public float autonomia;   // Quantos km o carro faz por litro de combustível

	// Observação: ainda não há métodos aqui (como ligar, acelerar, etc),
	// apenas a estrutura básica da classe.
	// Isso mostra o conceito de "abstração" — representar apenas o que é essencial.

}
